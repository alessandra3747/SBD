    IdOcena integer  NOT NULL,
    Przedmiot varchar2(64)  NOT NULL,
    Wartosc number(2,1)  NOT NULL,
    IdStudent integer  NOT NULL,
    CONSTRAINT Ocena_pk PRIMARY KEY (IdOcena)
) ;

CREATE TABLE Student (
    IdStudent integer  NOT NULL,
    Nazwisko varchar2(64)  NOT NULL,
    Rok integer  NOT NULL,
    CONSTRAINT Student_pk PRIMARY KEY (IdStudent)
) ;

-- foreign keys
ALTER TABLE Ocena ADD CONSTRAINT Ocena_Student
    FOREIGN KEY (IdStudent)
    REFERENCES Student (IdStudent);


